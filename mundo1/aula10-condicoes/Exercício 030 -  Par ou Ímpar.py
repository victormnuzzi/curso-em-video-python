# Crie um programa que leia um número inteiro e mostre na tela se ele é PAR ou ÍMPAR.
n = int(input('Digite um número: '))
resultado = n / 2
print(f'O número {n} é par.' if int(resultado) == resultado else f'O número {n} é ímpar.')

